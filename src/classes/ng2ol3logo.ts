export class Ng2ol3Logo {
    src: string;
    width: number;
    height: number;

    constructor() {

    }

    getSrc(): string {
        return this.src;
    }

    setSrc(src: string): void {
        this.src = src;
    }

    getWidth(): number {
        return this.width;
    }

    setWidth(width: number): void {
        this.width = width;
    }

    getHeight(): number {
        return this.height;
    }

    setHeight(height: number): void {
        this.height = height;
    }

}
